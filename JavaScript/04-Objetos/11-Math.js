/**
 *
 *
 *    M A T H
 *
 * Es un objeto incorporado que proporciona propiedades y métodos para realizar tareas matemáticas en números.
 * No es un objeto de función, lo que significa que no puedes instanciarlo como harías con un constructor de objetos.
 * En cambio, Math es un objeto estático que se utiliza directamente sin crear una instancia de él.
 *
 *  Algunos métodos de math:
 */
console.log(
    Math.PI,            // Devuelve PI.
    Math.abs(-14),      // Devuelve el valor absoluto de un número x.
    Math.round(34.7),   // Devuelve el valor absoluto redondeado.
    Math.floor(34.4),   // Devuelve el valor absoluto redondeado hacia abajo.
    Math.ceil(34.04),   // Devuelve el valor absoluto redondeado hacia arriba.
    Math.pow(2, 5),   // Devuelve la potencia de un número (base, exponente).
    Math.sqrt(144),   // Devuelve la raiz cuadrada de un número.
);

console.log(Math.random()); // Devuelve un número pseudoaleatorio entre 0 y 1.
